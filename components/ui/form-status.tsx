'use client'

import { useEffect, useState } from 'react'

interface FormStatusProps {
  message?: string
  type?: 'success' | 'error' | 'info'
}

export function FormStatus({ message, type = 'info' }: FormStatusProps) {
  const [announceMessage, setAnnounceMessage] = useState('')

  useEffect(() => {
    if (message) {
      // Delay slightly to ensure screen readers pick up the change
      setTimeout(() => setAnnounceMessage(message), 100)
    } else {
      setAnnounceMessage('')
    }
  }, [message])

  if (!message) return null

  const styles = {
    success: 'bg-green-50 border-green-200 text-green-800 dark:bg-green-900/20 dark:border-green-800 dark:text-green-200',
    error: 'bg-red-50 border-red-200 text-red-800 dark:bg-red-900/20 dark:border-red-800 dark:text-red-200',
    info: 'bg-blue-50 border-blue-200 text-blue-800 dark:bg-blue-900/20 dark:border-blue-800 dark:text-blue-200'
  }

  const icons = {
    success: (
      <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
        <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z" clipRule="evenodd" />
      </svg>
    ),
    error: (
      <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
        <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z" clipRule="evenodd" />
      </svg>
    ),
    info: (
      <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
        <path fillRule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z" clipRule="evenodd" />
      </svg>
    )
  }

  return (
    <>
      {/* Visual status message */}
      <div className={`rounded-xl border-2 p-4 ${styles[type]} animate-in slide-in-from-top-2 duration-300`} role="alert">
        <div className="flex items-start gap-3">
          <span className="flex-shrink-0">{icons[type]}</span>
          <p className="text-sm font-medium flex-1">{message}</p>
        </div>
      </div>

      {/* ARIA live region for screen readers - visually hidden but announced */}
      <div
        role="status"
        aria-live="polite"
        aria-atomic="true"
        className="sr-only"
      >
        {announceMessage}
      </div>
    </>
  )
}
